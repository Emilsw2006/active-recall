/* ─── Core i18n API ─── */

let currentLang = localStorage.getItem('ar_lang') || 'es';

/**
 * Return translated string for key.
 * Falls back to Spanish, then to the key itself.
 */
function T(key) {
  return TRANSLATIONS[currentLang]?.[key]
    ?? TRANSLATIONS.es[key]
    ?? key;
}

/** Translate a key in a specific language (not the current UI language). */
function TLang(key, lang) {
  return TRANSLATIONS[lang]?.[key]
    ?? TRANSLATIONS.es[key]
    ?? key;
}

/**
 * Return translated string with placeholder replacement.
 * Usage: TF('toast_uploading', {name: 'Bio.pdf'}) → 'Subiendo "Bio.pdf"...'
 */
function TF(key, replacements) {
  let str = T(key);
  for (const [k, v] of Object.entries(replacements)) {
    str = str.replace(`{${k}}`, v);
  }
  return str;
}

/**
 * Apply translations to all elements with data-i18n / data-i18n-ph attributes.
 * Also updates <html lang> and document.title.
 */
function applyLang(lang) {
  if (!TRANSLATIONS[lang]) return;
  currentLang = lang;
  localStorage.setItem('ar_lang', lang);

  // Text content (skip if it has aria/title only)
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = T(el.dataset.i18n);
  });

  // Aria-label
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    el.setAttribute('aria-label', T(el.dataset.i18nAria));
  });

  // Title
  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    el.title = T(el.dataset.i18nTitle);
  });

  // Placeholder
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    el.placeholder = T(el.dataset.i18nPh);
  });

  // <html lang>
  document.documentElement.lang = lang;

  // Highlight active language button
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

// Apply on first load
document.addEventListener('DOMContentLoaded', () => applyLang(currentLang));
